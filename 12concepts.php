  <TD align="left" valign=top><div align="center" class="pageHeading"> The Twelve Concepts for World Service </div>
      <div align="center" class="reprintText">(in the short form)</div>
      <div class="generalText"><br>
        The Twelve Concepts for World Service were written by A.A.'s co-founder Bill W., and were adopted by the General Service Conference of Alcoholics Anonymous in 1962. The Concepts are an interpretation of A.A.'s world service structure as it emerged through A.A.'s early history and experience. The short form of the Concepts reads:<br>
        <br>
            
        1. Final responsibility and ultimate authority for A.A. world services should always reside in the collective conscience of our whole Fellowship.<br>
        <br>
            
        2. The General Service Conference of A.A. has become, for nearly every practical purpose, the active voice and the effective conscience of our whole society in its world affairs.<br>
        <br>
        3. To insure effective leadership, we should endow each element of A.A.&#8212;the Conference, the General Service Board and its service corporations, staffs, committees, and executives&#8212;with a traditional &#8220;Right of Decision.&#8221;<br>
        <br>
        4. At all responsible levels, we ought to maintain a traditional &#8220;Right of Participation,&#8221; allowing a voting representation in reasonable proportion to the responsibility that each must discharge.<br>
        <br>
        5. Throughout our structure, a traditional &#8220;Right of Appeal&#8221; ought to prevail, so that minority opinion will be heard and personal grievances receive careful consideration.<br>
        <br>
            
        6. The Conference recognizes that the chief initiative and active responsibility in most world service matters should be exercised by the trustee members of the Conference acting as the General Service Board.<br>
        <br>
            
        7. The Charter and Bylaws of the General Service Board are legal instruments, empowering the trustees to manage and conduct world service affairs. The Conference Charter is not a legal document; it relies upon tradition and the A.A. purse for final effectiveness.<br>
        <br>
            
        8. The trustees are the principal planners and administrators of overall policy and finance. They have custodial oversight of the separately incorporated and constantly active services, exercising this through their ability to elect all the directors of these entities.<br>
        <br>
            
        9. Good service leadership at all levels is indispensable for our future functioning and safety. Primary world service leadership, once exercised by the founders, must necessarily be assumed by the trustees.<br>
        <br>
            
        10. Every service responsibility should be matched by an equal service authority, with the scope of such authority well defined.<br>
        <br>
            
        11. The trustees should always have the best possible committees, corporate service directors, executives, staffs, and consultants. Composition, qualifications, induction procedures, and rights and duties will always be matters of serious concern.<br>
        <br>
            
        12. The Conference shall observe the spirit of A.A. tradition, taking care that it never becomes the seat of perilous wealth or power; that sufficient operating funds and reserve be its prudent financial principle; that it place none of its members in a position of unqualified authority over others; that it reach all important decisions by discussion, vote, and whenever possible, substantial unanimity; that its actions never be personally punitive nor an incitement to public controversy; that it never perform acts of government; that, like the Society it serves, it will always remain democratic in thought and action.<br>&nbsp;      </div>
