  <TD align="left" valign=top><div align="center" class="pageHeading"> The Twelve Steps of Alcoholics Anonymous</div>
  <div class="reprintText" align="center">(in the short form)</div>
  <div class="generalText"><BR>1.&nbsp;&nbsp;We admitted we were powerless over alcohol&mdash;that our lives had become unmanageable.<br>
    <br>
    2.&nbsp;&nbsp;Came to believe that a Power greater than ourselves could restore us to sanity.<br>
    <br>
    3.&nbsp;&nbsp;Made a decision to turn our will and our lives over to the care of God <em>as we understood Him.<br>
    <br>
    </em>4.&nbsp;&nbsp;Made a searching and fearless moral inventory of ourselves.<br>
    <br>
    5.&nbsp;&nbsp;Admitted to God, to ourselves and to another human being the exact nature of our wrongs.<br>
    <br>
    6.&nbsp;&nbsp;Were entirely ready to have God remove all these defects of character.<br>
    <br>
    7.&nbsp;&nbsp;Humbly asked Him to remove our shortcomings.<br>
    <br>
    8.&nbsp;&nbsp;Made a list of all persons we had harmed, and became willing to make amends to them all.<br>
    <br>
    9.&nbsp;&nbsp;Made direct amends to such people wherever possible, except when to do so would injure them or others.<br>
    <br>
    10.&nbsp;&nbsp;Continued to take personal inventory and when we were wrong promptly admitted it.<br>
    <br>
    11.&nbsp;&nbsp;Sought through prayer and meditation to improve our conscious contact with God, <em>as we understood Him</em>, praying only for knowledge of His will for us and the power to carry that out.<br>
    <br>
    12.&nbsp;&nbsp;Having had a spiritual awakening as the result of these steps, we tried to carry this message to alcoholics, and to practice these principles in all our affairs.<br>
