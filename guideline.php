    <td align="center" valign="middle" class="pageHeading">Suffolk Intergroup Association Web Site Guidelines</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">1.	The Web site should be a tool in the accomplishment of our primary purpose: to stay sober and to help other alcoholics to achieve sobriety. To this end, the Web site should be used be used to disseminate information to members, as a contact point for Public Information work, and as a means of reaching out to the still suffering alcoholic.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">2.	We must always keep in mind that a Web site, by its nature, is always open to the non-member and the potential newcomer. We must always-present information on the Web site in such a way that it will not confuse people who are unfamiliar with AA as to what AA is and what it does.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">3.	Use only first names and last initials; no full names.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">4.	Do not list private phone numbers or e-mail addresses. Keep all contacts through the office phone, fax or mail, or through the Web site e-mail boxes.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">5.	Do not display any information about an individual that might be used to identify them, for example, "John P., Owner of the Greasy Spoon Diner".</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">6.	Do not link to unaffiliated (non AA) sites where the nature of the linked site might cause confusion regarding affiliation. (Map sites are specifically excluded from this prohibition as their use has become so ubiquitous on the Web, and their nature is such, that no one would mistake their use for affiliation.)</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">7.	No endorsement of non-AA literature or use of quotes from such literature. AA literature includes Conference approved books, pamphlets, tapes and videos, "The Grapevine", "Box 459", "The Link", the SIA "Bulletin", etc.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">8.	Where use is made of copyrighted, Conference approved literature or other materials on which AAWS, or Grapevine, or any other AA entity holds the copyright, always obtain permission for use (if required) and include copyright notices.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">9.	In the on-line meeting list, if a group meets in a rehab or other institution where a risk of seeming affiliation exists, don't use the name of the institution in the meeting address, replace it with the street address (and building description, if necessary). For example: "157 Broadway, in patient cafeteria".</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">10.	List institutional meeting on a separate page with a non-affiliation statement on that page. Do not list restricted meetings.</td>
  </tr>
  <tr>
    <td valign="top"></td>
    <td class="generalText">&nbsp;</td>
  </tr>
  <tr>
    <td width="10" valign="top"></td>
    <td class="generalText">11.	To avoid the conflict between appearing to endorse practices of which we do not approve which other AA services entities may use on their Web sites, and harming the spirit of the First Tradition by not linking to some sites while linking to others, we will provide links to no AA Web site other then that of AAWS, The Grapevine, Suffolk General Service and SENY.</td>
  </tr>
