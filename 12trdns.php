<tr><TD align="left" valign=top><div class="pageHeading" align="center">The Twelve Traditions Of Alcoholics Anonymous</div>
  <div class="reprintText" align="center">(in the short form)</div>
         <div class="generalText"><BR>1.&nbsp;&nbsp;Our common welfare should come first; personal recovery depends upon A.A. unity.<br>
           <br>
         2.&nbsp;&nbsp;For our group purpose there is but one ultimate authority&mdash;a loving God as He may express Himself in our group conscience. Our leaders are but trusted servants; they do not govern.<br>
         <br>
         3.&nbsp;&nbsp;The only requirement for A.A. membership is a desire to stop drinking.<br>
         <br>
         4.&nbsp;&nbsp;Each group should be autonomous except in matters affecting other groups or A.A. as a whole.<br>
         <br>
         5.&nbsp;&nbsp;Each group has but one primary purpose&mdash; to carry its message to the alcoholic who still suffers.<br>
         <br>
         6.&nbsp;&nbsp;An A.A. group ought never endorse, finance, or lend the A.A. name to any related facility or outside enterprise, lest problems of money, property, and prestige divert us from our primary purpose.<br>
         <br>
         7.&nbsp;&nbsp;Every A.A. group ought to be fully selfsupporting, declining outside contributions.<br>
         <br>
         8.&nbsp;&nbsp;Alcoholics Anonymous should remain forever non-professional, but our service centers may employ special workers.<br>
         <br>
         9.&nbsp;&nbsp;A.A., as such, ought never be organized; but we may create service boards or committees directly responsible to those they serve.<br>
         <br>
         10.&nbsp;&nbsp;Alcoholics Anonymous has no opinion on outside issues; hence the A.A. name ought never be drawn into public controversy.<br>
         <br>
         11.&nbsp;&nbsp;Our public relations policy is based on attraction rather than promotion; we need always maintain personal anonymity at the level of press, radio, and films.<br>
         <br>
         12.&nbsp;&nbsp;Anonymity is the spiritual foundation of all our traditions, ever reminding us to place principles before personalities.<br>
